package model.vo;

public class ServicoVO {
	private int idServico;
	private String descServico;
	private double precoMaoObra;
	
	public ServicoVO(int idServico, String descServico, double precoMaoObra) {
		this.idServico = idServico;
		this.descServico = descServico;
		this.precoMaoObra = precoMaoObra;
	}
	
	
	public int getIdServico() {
		return idServico;
	}

	public String getDescServico() {
		return descServico;
	}
	public void setDescServico(String descServico) {
		this.descServico = descServico;
	}
	public double getPrecoMaoObra() {
		return precoMaoObra;
	}
	public void setPrecoMaoObra(double precoMaoObra) {
		this.precoMaoObra = precoMaoObra;
	}
}
